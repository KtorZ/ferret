import store from '../store';
import { readonly } from 'svelte/store';

// -------------------------------------------------------------------------------------------------
// Svelte Store
// -------------------------------------------------------------------------------------------------

const DEFAULT_STATE = {
  EUR: 0.1465,
  USD: 0.1696,
  CHF: 0.1350,
  GBP: 0.1265,
};

const state = store.writable(
  'ferret.fx.v1',
  DEFAULT_STATE,
);

// -------------------------------------------------------------------------------------------------
// Interface with the application
// -------------------------------------------------------------------------------------------------

function reset() {
  state.set(DEFAULT_STATE);
}

// -------------------------------------------------------------------------------------------------
// Exports
// -------------------------------------------------------------------------------------------------

export default {
  state: readonly(state),
  reset,
};
