import store from '../store';
import { readonly } from 'svelte/store';

// -------------------------------------------------------------------------------------------------
// Svelte Store
// -------------------------------------------------------------------------------------------------

const DEFAULT_STATE = {
  EUR: 0.2223,
  USD: 0.2584,
  CHF: 0.2013,
  GBP: 0.1925,
};

const state = store.writable(
  'ferret.fx.v1',
  DEFAULT_STATE,
);

// -------------------------------------------------------------------------------------------------
// Interface with the application
// -------------------------------------------------------------------------------------------------

function reset() {
  state.set(DEFAULT_STATE);
}

// -------------------------------------------------------------------------------------------------
// Exports
// -------------------------------------------------------------------------------------------------

export default {
  state: readonly(state),
  reset,
};
